;比较字符串是否相同（非串指令）
DATAS SEGMENT
    BUF1 DB 'ABCDEFG$'
    BUF2 DB 'ABCDEFS$'
    COUNT EQU $-BUF2 
    BUF3 DB 'SAME!$'
    BUF4 DB 'NOT SAME!$'
DATAS ENDS

STACKS SEGMENT

STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS,ES:DATAS
START:
    MOV AX,DATAS
    MOV DS,AX
    MOV ES,AX

    LEA SI,BUF1
    LEA DI,BUF2
    MOV CX,COUNT
LOOP1:
	MOV AL,[DI]
	CMP [SI],AL
	JNZ NOTSAME
	INC SI
	INC DI
	LOOP LOOP1
	MOV AX,1
	LEA DX,BUF3
	JMP PRINT
NOTSAME:
	MOV AX,0
	LEA DX,BUF4
PRINT:
    MOV AH,9H
	INT 21H
    MOV AH,4CH
    INT 21H
CODES ENDS
    END START
