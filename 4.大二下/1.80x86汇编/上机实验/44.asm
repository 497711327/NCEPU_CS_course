
;用子程序复制字符串
DATAS SEGMENT
    BUF DB 'THIS IS A STRING$'
    COUNT EQU $-BUF
    COPY DB COUNT DUP(?) 
DATAS ENDS

STACKS SEGMENT

STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS,ES:DATAS

START:
    MOV AX,DATAS
    MOV DS,AX
    MOV ES,AX

    LEA SI,BUF
    LEA DI,COPY
    MOV CX,COUNT

    CALL COPYY

    LEA DX,COPY
    MOV AH,9H             ;显示字符串
    INT 21H

    MOV AH,4CH
    INT 21H
  

COPYY PROC NEAR
	CLD
	REP MOVSB             ;只是把复制的串指令放入子程序中
	RET
COPYY ENDP

CODES ENDS
    END START
