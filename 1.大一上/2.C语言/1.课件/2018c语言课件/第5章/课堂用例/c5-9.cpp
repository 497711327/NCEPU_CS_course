/*将整型变量a中高字节和低字节中的内容对调。*/
#include "stdio.h"
void main(void)
{
	int a=0x1122,b,c;
	printf("a=0x%x\n",a);  /*用16进制形式输出a的值*/
	b = (a&0xff)<<8;     /*高字节清0，低字节左移8位到高字节，存入变量b*/      
	c = (a&0xff00)>>8;    /*低字节清0，高字节右移8位到低字节，存入变量c*/
	a = b|c;               /*实现高字节和低字节的对调*/
	printf("对调后：0x%x", a);
}
